var builder = WebApplication.CreateBuilder(args);

builder.Services.AddControllers(); // this service will add controller folder or class controller

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddCors((options) => { 
    options.AddPolicy("DevCors",
    (corsBuilder) =>
    {
        corsBuilder
            .WithOrigins("http://localhost:4200", "http://localhost:3000", "http://localhost:8000")
            .AllowAnyMethod()
            .AllowAnyHeader()
            .AllowCredentials();
    });
     options.AddPolicy("ProdCors",
    (corsBuilder) =>
    {
        corsBuilder
            .WithOrigins("https//:MyProductionSite.com")
            .AllowAnyMethod()
            .AllowAnyHeader()
            .AllowCredentials();
    });
});

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseCors("DevCors");
    app.UseSwagger();
    app.UseSwaggerUI();
}
// it was change to else because if we are using environment we dont need https for dev
else
{
    app.UseCors("ProdCors");
    app.UseHttpsRedirection();
}

app.MapControllers();

